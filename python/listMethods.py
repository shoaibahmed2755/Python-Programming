#Adding elements inside the list 
#  1.append() --> add 1 ele at the last of the list 
#  2.insert() --> add 1 ele at the specified position
#  3.extend() --> add multiple elements at the last of the list
a=[3,11,56,67,45,90,"abc"]
print(a)
a.append(100)
print(a)
a.append(True)
print(a)
a.append([1,2])
print(a)